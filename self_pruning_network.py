# Cell 2: This is the heart of the project - a linear layer that can kill its own weights

class PrunableLinear(nn.Module):
    """
    A linear layer that learns which weights to keep and which to kill.

    How it works:
    - We have normal 'weight' matrix
    - We have 'gate_scores' (same shape as weight)
    - Gates = sigmoid(gate_scores) → values between 0 and 1
    - Pruned weights = weight * gates
    - If gate is 0, that weight is DEAD (doesn't contribute)
    - If gate is 1, weight is ALIVE (fully used)

    The L1 loss on gates pushes them toward 0 (dead)
    But classification loss wants useful weights to stay alive
    """

    def __init__(self, in_features, out_features, bias=True):
        super().__init__()

        self.in_features = in_features
        self.out_features = out_features

        # The actual weights (these learn the patterns)
        self.weight = nn.Parameter(torch.randn(out_features, in_features) * 0.1)

        # The gate scores (these learn what to kill)
        # Starting near 0 means sigmoid(0)=0.5 → half alive, half dead initially
        self.gate_scores = nn.Parameter(torch.zeros(out_features, in_features))

        # Bias (optional)
        if bias:
            self.bias = nn.Parameter(torch.zeros(out_features))
        else:
            self.bias = None

    def forward(self, x):
        # Step 1: Convert gate scores to gates (0 to 1)
        gates = torch.sigmoid(self.gate_scores)

        # Step 2: Kill weights by multiplying with gates
        pruned_weights = self.weight * gates

        # Step 3: Normal linear operation with DEAD weights
        output = F.linear(x, pruned_weights, self.bias)

        # Store gates so we can calculate sparsity loss later
        self.last_gates = gates

        return output

    def get_gates(self):
        """Return current gate values (0=dead, 1=alive)"""
        return torch.sigmoid(self.gate_scores)

    def count_dead_weights(self, threshold=0.01):
        """Count how many weights are basically dead (gate < threshold)"""
        with torch.no_grad():
            gates = torch.sigmoid(self.gate_scores)
            dead = (gates < threshold).float().mean().item()
            return dead * 100  # return percentage

# Cell 3:

class SelfPruningNetwork(nn.Module):
    """
    A 4-layer network that prunes itself.

    Architecture:
    Input: 3072 pixels (32x32x3 CIFAR image flattened)
    Layer 1: 3072 → 512  (big layer, lots of pruning possible)
    Layer 2: 512 → 256
    Layer 3: 256 → 128
    Layer 4: 128 → 10   (output classes)

    I made it smaller than original so it runs faster on CPU
    """

    def __init__(self):
        super().__init__()
        self.flatten = nn.Flatten()


        self.fc1 = PrunableLinear(3072, 512)   # Big layer
        self.fc2 = PrunableLinear(512, 256)    # Medium layer
        self.fc3 = PrunableLinear(256, 128)    # Small layer
        self.fc4 = PrunableLinear(128, 10)     # Output layer

        # Regularization
        self.dropout = nn.Dropout(0.3)
        self.relu = nn.ReLU()

    def forward(self, x):
        x = self.flatten(x)

        x = self.relu(self.fc1(x))
        x = self.dropout(x)

        x = self.relu(self.fc2(x))
        x = self.dropout(x)

        x = self.relu(self.fc3(x))

        x = self.fc4(x)

        return x

    def get_total_sparsity_loss(self):
        """Calculate L1 penalty = sum of all gate values"""
        total = 0
        total += self.fc1.get_gates().sum()
        total += self.fc2.get_gates().sum()
        total += self.fc3.get_gates().sum()
        total += self.fc4.get_gates().sum()
        return total

    def get_overall_sparsity(self, threshold=0.01):
        """Calculate what percentage of weights are dead"""
        all_gates = []
        all_gates.append(self.fc1.get_gates().flatten())
        all_gates.append(self.fc2.get_gates().flatten())
        all_gates.append(self.fc3.get_gates().flatten())
        all_gates.append(self.fc4.get_gates().flatten())

        all_gates = torch.cat(all_gates)
        dead = (all_gates < threshold).float().mean().item()
        return dead * 100

    def get_all_gates_numpy(self):
        """Get all gate values as numpy array (for plotting)"""
        gates_list = []
        gates_list.extend(self.fc1.get_gates().cpu().detach().numpy().flatten())
        gates_list.extend(self.fc2.get_gates().cpu().detach().numpy().flatten())
        gates_list.extend(self.fc3.get_gates().cpu().detach().numpy().flatten())
        gates_list.extend(self.fc4.get_gates().cpu().detach().numpy().flatten())
        return np.array(gates_list)

# Cell 4:
def get_cifar_loaders(batch_size=64):
    """
    Load CIFAR-10 dataset.
    CIFAR-10 has 50k training images, 10k test images.
    Images are 32x32 color photos of 10 classes:
    airplane, car, bird, cat, deer, dog, frog, horse, ship, truck
    """

    mean = [0.4914, 0.4822, 0.4465]
    std = [0.2023, 0.1994, 0.2010]

    train_transform = transforms.Compose([
        transforms.RandomHorizontalFlip(),  # Flip images left-right randomly
        transforms.RandomCrop(32, padding=4),  # Slightly random crops
        transforms.ToTensor(),  # Convert to PyTorch tensor
        transforms.Normalize(mean, std)
    ])

    test_transform = transforms.Compose([
        transforms.ToTensor(),
        transforms.Normalize(mean, std)
    ])

    print("Downloading CIFAR-10 (this happens only once)...")
    train_dataset = torchvision.datasets.CIFAR10(
        root='./data', train=True, download=True, transform=train_transform
    )
    test_dataset = torchvision.datasets.CIFAR10(
        root='./data', train=False, download=True, transform=test_transform
    )

    train_loader = DataLoader(train_dataset, batch_size=batch_size,
                              shuffle=True, num_workers=0)  # num_workers=0 for CPU
    test_loader = DataLoader(test_dataset, batch_size=batch_size,
                             shuffle=False, num_workers=0)

    print(f"Training samples: {len(train_dataset)}")
    print(f"Test samples: {len(test_dataset)}")

    return train_loader, test_loader

# Cell 5:

def train_for_lambda(lambda_val, epochs=15, batch_size=64, learning_rate=0.001):
    """
    Train the self-pruning network with a specific sparsity penalty.

    Parameters:
    - lambda_val: how hard we push for sparsity (higher = more pruning)
    - epochs: training iterations (reduced for CPU)
    - batch_size: how many images per batch
    - learning_rate: step size for optimizer
    """

    print("\n" + "="*60)
    print(f"🔥 TRAINING WITH λ = {lambda_val}")
    print("="*60)
    print(f"λ controls sparsity: higher λ = more dead weights")
    print(f"Training on CPU - this will take ~2-3 minutes per λ")
    print("-"*60)

    train_loader, test_loader = get_cifar_loaders(batch_size)

    model = SelfPruningNetwork().to(device)

    # Adam optimizer
    optimizer = optim.Adam(model.parameters(), lr=learning_rate)

    # Loss for classification
    class_loss_fn = nn.CrossEntropyLoss()

    history = {
        'train_loss': [],
        'test_acc': [],
        'sparsity': []
    }

    for epoch in range(epochs):
        model.train()
        total_train_loss = 0

        pbar = tqdm(train_loader, desc=f"Epoch {epoch+1}/{epochs}")
        for images, labels in pbar:
            images, labels = images.to(device), labels.to(device)

            outputs = model(images)

            class_loss = class_loss_fn(outputs, labels)
            sparsity_loss = model.get_total_sparsity_loss()
            total_loss = class_loss + lambda_val * sparsity_loss

            optimizer.zero_grad()
            total_loss.backward()
            optimizer.step()

            total_train_loss += total_loss.item()

            pbar.set_postfix({
                'loss': f'{total_loss.item():.3f}',
                'class': f'{class_loss.item():.3f}'
            })

        avg_train_loss = total_train_loss / len(train_loader)

        model.eval()
        correct = 0
        total = 0

        with torch.no_grad():  # No gradients needed for testing
            for images, labels in test_loader:
                images, labels = images.to(device), labels.to(device)
                outputs = model(images)
                _, predicted = torch.max(outputs, 1)
                total += labels.size(0)
                correct += (predicted == labels).sum().item()

        test_accuracy = 100 * correct / total
        current_sparsity = model.get_overall_sparsity()

        history['train_loss'].append(avg_train_loss)
        history['test_acc'].append(test_accuracy)
        history['sparsity'].append(current_sparsity)

        print(f"Epoch {epoch+1}: Train Loss={avg_train_loss:.4f}, "
              f"Test Acc={test_accuracy:.2f}%, "
              f"Sparsity={current_sparsity:.1f}%")

    final_sparsity = model.get_overall_sparsity()
    final_accuracy = max(history['test_acc'])  # Best accuracy during training

    print("\n" + "-"*60)
    print(f"✅ FINAL RESULTS for λ={lambda_val}:")
    print(f"   Best Test Accuracy: {final_accuracy:.2f}%")
    print(f"   Final Sparsity: {final_sparsity:.1f}% (weights are dead)")
    print(f"   That means {final_sparsity:.1f}% of weights are essentially zero!")
    print("-"*60)

    return model, history, final_accuracy, final_sparsity

